#!/bin/bash

echo "🎨 Installing 3D and Animation Libraries for Gas Power v2..."
echo "=================================================="

# Framer Motion - animazioni fluide e gesture
npm install framer-motion

# React Spring - animazioni fisiche realistiche
npm install @react-spring/web @react-spring/three

# React Parallax - effetti parallax avanzati
npm install react-parallax react-parallax-tilt

# React Intersection Observer - trigger animazioni on scroll
npm install react-intersection-observer

# Lottie React - animazioni JSON professionali
npm install lottie-react

# Three.js per React - effetti 3D avanzati
npm install three @react-three/fiber @react-three/drei

# GSAP - animazioni professionali (optional ma potente)
npm install gsap

# React Typed - effetto macchina da scrivere
npm install react-typed-component

# React CountUp - numeri animati
npm install react-countup

# Particles.js per React - effetti particelle
npm install react-tsparticles tsparticles

# AOS - Animate on Scroll
npm install aos @types/aos

# React Fast Marquee - ticker animato
npm install react-fast-marquee

echo ""
echo "✅ All 3D and animation libraries installed!"
echo ""
echo "Libraries installed:"
echo "- ✨ Framer Motion: Professional animations"
echo "- 🎭 React Spring: Physics-based animations"
echo "- 🏔️ React Parallax: Parallax effects"
echo "- 👁️ Intersection Observer: Scroll triggers"
echo "- 🎬 Lottie: Professional JSON animations"
echo "- 🎮 Three.js: 3D graphics"
echo "- 🚀 GSAP: Advanced animations"
echo "- ⌨️ React Typed: Typewriter effect"
echo "- 🔢 React CountUp: Animated numbers"
echo "- ✨ Particles: Particle effects"
echo "- 📜 AOS: Animate on scroll"
echo "- 🎪 Fast Marquee: Smooth ticker"
